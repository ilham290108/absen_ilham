<?php

namespace App\Http\Controllers;

use App\Models\User;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Auth;
use Illuminate\Support\Facades\Hash;

class AuthController extends Controller
{
    // Middleware untuk membatasi akses halaman login dan register hanya untuk guest
 

    public function showLoginForm()
    {
        return view('layouts.Auth.login');
    }

    public function login(Request $request)
    {
        $request->validate([
            'email' => 'required|email',
            'password' => 'required',
        ]);

        if (Auth::attempt($request->only('email', 'password'))) {
            return redirect()->intended('');
        }

        return back()->withErrors([
            'email' => 'The provided credentials do not match our records.',
        ]);
    }

    public function showRegistrationForm()
    {
        return view('layouts.Auth.register');
    }

    public function register(Request $request)
    {
        $request->validate([
            'name' => 'required|string|max:255',
            'email' => 'required|string|email|max:255|unique:users',
            'password' => 'required|string|min:8|confirmed',
        ]);

        User::create([
            'name' => $request->name,
            'email' => $request->email,
            'password' => Hash::make($request->password),
            'role' => 'guru',
        ]);

        return redirect()->route('login')->with('success', 'Registration successful. You can now login.');
    }

    public function logout(Request $request)
{
    Auth::logout(); // Logout user

    $request->session()->invalidate(); // Invalidate session lama
    $request->session()->regenerateToken(); // Bikin CSRF token baru

    return redirect('/login'); // Arahkan ke login
}

}